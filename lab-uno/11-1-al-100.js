/**
 * Ejercicios de Bucles (for/while)
 * Ejercicio 11: Escribe una función que imprima los números del 1 al 100 en la
    consola.
 */

function unoCien(){
    console.log("-----------------------------------------------------------")
    for (let i = 1; i <= 100; i++) {
        console.log(i);
    }
}